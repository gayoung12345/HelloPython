def korean_number(x):
    if(x == 1) :
        print('일')
    elif(x == 2) :
        print('이')
    elif(x == 3) :
        print('삼')
    elif(x == 4) :
        print('사')
    elif(x == 5) :
        print('오')
    elif(x == 6) :
        print('육')
    elif(x == 7) :
        print('칠')
    elif(x == 8) :
        print('팔')
    elif(x == 9) :
        print('구')
    elif(x == 10) :
        print('십')
    else : 
        print('1~10사이 숫자만 입력')

korean_number(1)
korean_number(3)
korean_number(10)


